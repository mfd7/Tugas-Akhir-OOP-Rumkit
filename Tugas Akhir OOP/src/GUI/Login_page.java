    private void btn_loginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_loginMouseClicked
        String uname = "";
        String pass = "";
        ConnectionManager conMan = new ConnectionManager();
        Connection conn = conMan.logOn();
        try {
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("Select staff_id, staff_password from staff");
            while(rs.next()){
                uname = rs.getString("staff_id");
                pass = rs.getString("staff_password");
            }
        } catch (SQLException ex) {
        }
        if(text_uname.getText().equals(uname) && String.valueOf(text_pass.getPassword()).equals(pass)){
            Dashboard_page dash = new Dashboard_page();
            dash.setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Username atau Password Salah!");
        }
    }//GEN-LAST:event_btn_loginMouseClicked

